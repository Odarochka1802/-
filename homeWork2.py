# Для решения этой задачи можно использовать императивную парадигму,
# так как нам нужно последовательно выполнить набор инструкций для
# создания таблицы умножения. Мы можем использовать циклы для итерации
# по числам от 1 до n и вывода результатов умножения.


def multiplication_table(n):
    try:
        n = int(n)
        for i in range(1, n+1):
            for j in range(1, n+1):
                print(f"{i} * {j} = {i*j}")
            print()  # Пустая строка между таблицами для разделения
    except ValueError:
        raise ValueError("Введенное значение не является целым числом")

n = input("Введите целое число: ")
try:
    multiplication_table(n)
except ValueError as e:
    print(e)
